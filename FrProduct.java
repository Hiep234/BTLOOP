    public boolean  checkValidateForm(){
       if(txtGiaBan.getText().isEmpty()||txtMaSP.getText().isEmpty()
               ||txtNgayNhap.getText().isEmpty()
               ||txtSoLuong.getText().isEmpty()
               ||txtTenSP.getText().isEmpty()
               ||txtSoGio.getText().isEmpty()
               ||txtSoDienThoai.getText().isEmpty()) {
           return false;
       }
       return true;
    }
    public KhachHang getModel() throws ParseException{
        KhachHang sp = new KhachHang();
        sp.setMaSp(txtMaSP.getText().trim());
        sp.setTenSP(txtTenSP.getText().trim());
        sp.setGia(Double.parseDouble(txtGiaBan.getText().trim()));
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText().trim()));
        sp.setSoGio(Integer.parseInt(txtSoGio.getText().trim()));
        sp.setLoaiSp((LoaiXe)cboLoaiSP.getSelectedItem());
        sp.setNgayNhap(date_Format.parse(txtNgayNhap.getText().trim()));
        sp.setSoDienThoai(txtSoDienThoai.getText().trim());
        return sp;
    }
    
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbSanPham.getModel();
        tbModel.setRowCount(0);
        for(KhachHang sp : daoSP.getAllSanPham()){
            Object [] rowData = new Object[9];
            rowData[0] = sp.getMaSp();
            rowData[1] = sp.getTenSP();
            rowData[2] = sp.getSoDienThoai();
            rowData[3] = date_Format.format(sp.getNgayNhap());
            rowData[4] = sp.getLoaiSp();
            rowData[5] = sp.getSoLuong();
            rowData[6] = sp.getSoGio();
            rowData[7] = sp.getGia();
            rowData[8] = sp.getSoLuong()*sp.getGia()*sp.getSoGio();
            tbModel.addRow(rowData);
        }
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(!checkValidateForm()){
        JOptionPane.showMessageDialog(this,"Bạn chưa nhập đầy đủ thông tin");
    } else {
        try {
            KhachHang sp = getModel();

            if (btnSave.getText().equals("LƯU")) {
                if (daoSP.add(sp) > 0) {
                    lblStatus.setText("Lưu dữ liệu thành công");
                    lblStatus.setForeground(Color.blue);
                    fillData();
                } else {
                    lblStatus.setText("Lưu dữ liệu thất bại");
                    lblStatus.setForeground(Color.red);
                }
            } else { // Trường hợp nút "Sửa"
                if (daoSP.edit(sp) > 0) {
                    lblStatus.setText("Sửa dữ liệu thành công");
                    lblStatus.setForeground(Color.blue);
                    fillData();
                } else {
                    lblStatus.setText("Sửa dữ liệu thất bại");
                    lblStatus.setForeground(Color.red);
                }
                // Reset lại nút và form sau khi sửa
                btnSave.setText("LƯU");
                
            }

        } catch (ParseException ex) {
            System.out.println("Lỗi: " + ex.toString());
        }
    }
    }//GEN-LAST:event_btnSaveActionPerformed
