    private void btnThongKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThongKeActionPerformed
   
   Map<String, Double> brandTotalPriceMap = new HashMap<>();
    Map<String, Integer> brandTotalQuantityMap = new HashMap<>();
    lblStatus.setText("Thống kê thành công");
        lblStatus.setForeground(Color.blue);

    // Calculate total price and quantity for each brand
    for (KhachHang kh : daoSP.getAllSanPham()) {
        String brand = kh.getLoaiSp().getName();
        double totalPrice = kh.getSoLuong() * kh.getGia()*kh.getSoGio();

        brandTotalPriceMap.put(brand, brandTotalPriceMap.getOrDefault(brand, 0.0) + totalPrice);
        brandTotalQuantityMap.put(brand, brandTotalQuantityMap.getOrDefault(brand, 0) + kh.getSoLuong());
    }

    // Display statistics in a new table
    DefaultTableModel brandStatModel = new DefaultTableModel(
            new Object[][]{},
            new String[]{"Loại xe", "Tổng số lượng", "Tổng giá"}
    );

    for (Map.Entry<String, Double> entry : brandTotalPriceMap.entrySet()) {
        String brand = entry.getKey();
        int totalQuantity = brandTotalQuantityMap.get(brand);
        double totalPrice = entry.getValue();

        brandStatModel.addRow(new Object[]{brand, totalQuantity, totalPrice});
    }

    JTable brandStatTable = new JTable(brandStatModel);
    brandStatTable.setRowHeight(27);

    JScrollPane brandStatScrollPane = new JScrollPane(brandStatTable);

    JDialog brandStatDialog = new JDialog(this, "Bảng thống kê", true);
    brandStatDialog.getContentPane().add(brandStatScrollPane, BorderLayout.CENTER);

    brandStatDialog.setSize(400, 300);
    brandStatDialog.setLocationRelativeTo(this);
    brandStatDialog.setVisible(true);
    }//GEN-LAST:event_btnThongKeActionPerformed
    
